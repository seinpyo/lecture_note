package day02;

public class JavaThird {
	public static void main(String[] args) {
		/*
		 \t : tab (한번 탭하면 8byte 씩)
		 \' : 작은 따옴표 출력
		 \" : 큰 따옴표 출력
		 %% : % 출력
		 \\ : \ 출력
		 \b : 백스페이스 - 커서 왼쪽으로 이동하면서 앞글자를 한글자씩 삭제 (이클립스 콘솔창에서는 정상 작동하지 않는다.)
		 */
		System.out.printf("\\n : new line \n");
		System.out.printf("\\t : tab \n");
		System.out.printf("\\' : \' 출력\n");
		System.out.printf("\\\" : \" 출력\n");
		System.out.printf("%%%% : %% 출력\n");
		System.out.printf("\\\\ : \\ 출력\n");
		System.out.printf("\\b : 백스페이스\n");
		
	}
}
